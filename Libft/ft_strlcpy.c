#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	srclength;

	srclength = ft_strlen(src);
	if (srclength + 1 < dstsize)
	{
		ft_memcpy(dst, src, srclength + 1);
	}
	else if (dstsize != 0)
	{
		ft_memcpy(dst, src, dstsize - 1);
		dst[dstsize - 1] = '\0';
	}
	return (srclength);
}
